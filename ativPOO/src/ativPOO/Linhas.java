package ativPOO;

public class Linhas {
	String numFone;
	String nmUser;
	String dataInstall;
	String endereco;
	Boolean internet;
	int tipoLinha;
	
	public int getTipoLinha() {
		return tipoLinha;
	}
	public void setTipoLinha(int tipoLinha) {
		this.tipoLinha = tipoLinha;
	}
	public String getNumFone() {
		return numFone;
	}
	public void setNumFone(String numFone) {
		this.numFone = numFone;
	}
	public String getNmUser() {
		return nmUser;
	}
	public void setNmUser(String nmUser) {
		this.nmUser = nmUser;
	}
	public String getDataInstall() {
		return dataInstall;
	}
	public void setDataInstall(String dataInstall) {
		this.dataInstall = dataInstall;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public Boolean getInternet() {
		return internet;
	}
	public void setInternet(Boolean internet) {
		this.internet = internet;
	}
	
	
}
